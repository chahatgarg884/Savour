function showlogin(){
    document.getElementById("signup").style.display='none';
    document.getElementById("login").style.display='flex';
    
}
function showsignup(){
    document.getElementById("login").style.display='none';
    document.getElementById("signup").style.display='flex';
}
function closed(){
    document.getElementById("login").style.display='none';
    document.getElementById("signup").style.display='none';
}